package com.loop;

public class Loop7 {

	public static void main(String[] args) {
		
	    for (int i = 5; i<=5 ; i--) { //i=i+1//outer loop//row
		
		for (int j = 1; j<=i; j++) { //j=j+1//inner loop//column
			
			System.out.print(j);
		
		}
		
		
		System.out.println(i);
		
		
		
	}
			
			
			
		
		}
		
		
	}

		
		
		


	
	
	
	

