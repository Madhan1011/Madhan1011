package com.loop;

public class Loop1 { //Nested Loop

	public static void main(String[] args) {
		
	    for (int i = 0; i <2 ;i++) { //i=i+1//outer loop//row
		
		for (int j = 0; j <2; j++) { //j=j+1//inner loop//column
			
			System.out.println(i+" "+j);
		
		}
		
		
		
		
		
	
			
			
			
		
		}
		
		
	}

	
	
	
	
}
